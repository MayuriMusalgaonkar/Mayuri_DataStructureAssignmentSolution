package com.learning.model;

public class Floor {

	 public int size;
	 public int day;
	
	
	public Floor() {
		super();
	}

	//getter setter 
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
}
	
	

